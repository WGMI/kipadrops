  $(window).scroll(function(){
    var bannerTop = $('.banner').offset().top - 50;
    var footballTop = $('.football-field').offset().top; 
    var whatHowTop = $('.what-how-sec').offset().top;
    var scrollTop = $(this).scrollTop() + 450;
    // Football Feild
    var bannerH = bannerTop + $('.banner').outerHeight() - 150;
    if(scrollTop - 450 > bannerTop){
      $('.banner').addClass('animation')
    }
    if(scrollTop - 450 > bannerH || scrollTop - 450 < bannerTop){
      $('.banner').removeClass('animation')
    }
    // Football Feild
    var footballH = footballTop + $('.football-field').outerHeight();
    if(scrollTop > footballTop){
      $('.football-field').addClass('animation')
    }
    if(scrollTop > footballH || scrollTop < footballTop){
      $('.football-field').removeClass('animation')
    }

    // what how sec
    var whatHowH = whatHowTop + $('.what-how-sec').outerHeight();
    if(scrollTop > whatHowTop + 300){
      $('.what-how-sec').addClass('animation')
    }
    if(scrollTop > whatHowH || scrollTop < whatHowTop + 300){
      $('.what-how-sec').removeClass('animation')
